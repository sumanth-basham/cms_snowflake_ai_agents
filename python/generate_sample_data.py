import random
import string
import uuid
from datetime import date, timedelta
from typing import Dict, List

import pandas as pd


def _random_id(prefix: str, length: int = 8) -> str:
    return f"{prefix}{''.join(random.choices(string.digits, k=length))}"


def _random_date(start: date, end: date) -> date:
    delta = (end - start).days
    return start + timedelta(days=random.randint(0, delta))


def _random_ndc() -> str:
    """Generate a plausible-format synthetic NDC (11-digit)."""
    return "".join(random.choices(string.digits, k=11))


def generate_members(n: int = 200, seed: int = 42) -> pd.DataFrame:
    """
    Generate synthetic member records.
    Grain: one row per member.
    PHI level: HIGH (member_id is a synthetic surrogate, not real MBI).
    """
    random.seed(seed)
    states = ["CA", "TX", "FL", "NY", "PA", "OH", "IL", "GA", "NC", "MI"]
    genders = ["M", "F", "U"]
    lics = ["LIS_FULL", "LIS_PARTIAL", "NON_LIS"]
    rows = []
    for _ in range(n):
        rows.append(
            {
                "member_id": _random_id("MBR"),
                "contract_id": _random_id("H", 4),
                "plan_id": _random_id("PBP", 3),
                "state_code": random.choice(states),
                "birth_year": random.randint(1930, 1958),
                "gender_code": random.choice(genders),
                "low_income_subsidy_code": random.choice(lics),
                "enrollment_start_date": str(_random_date(date(2023, 1, 1), date(2024, 1, 1))),
                "enrollment_end_date": None,
                "is_active": True,
            }
        )
    return pd.DataFrame(rows)


def generate_pharmacy_claims(members_df: pd.DataFrame, n_per_member: int = 15, seed: int = 42) -> pd.DataFrame:
    """
    Generate synthetic pharmacy claims.
    Grain: one row per dispensing event (fill).
    PHI level: HIGH (member_id links to member record).

    NOTE: Drug classes and NDCs are synthetic and not validated against
    real formulary or CMS specifications.
    """
    random.seed(seed)
    drug_classes = [
        "STATIN", "ACE_INHIBITOR", "ARB", "SULFONYLUREA",
        "OPIOID", "BENZO", "ANTICHOLINERGIC", "WARFARIN",
        "NSAID", "ANTIDEPRESSANT", "ANTIPSYCHOTIC", "INSULIN",
        "METFORMIN", "DIURETIC", "BETA_BLOCKER",
    ]
    days_supply_options = [30, 60, 90]
    rows = []
    claim_date_start = date(2024, 1, 1)
    claim_date_end = date(2024, 12, 31)

    for _, member in members_df.iterrows():
        for _ in range(n_per_member):
            drug_class = random.choice(drug_classes)
            days_supply = random.choice(days_supply_options)
            fill_date = _random_date(claim_date_start, claim_date_end)
            rows.append(
                {
                    "claim_id": str(uuid.uuid4()),
                    "member_id": member["member_id"],
                    "contract_id": member["contract_id"],
                    "plan_id": member["plan_id"],
                    "ndc": _random_ndc(),
                    "drug_class": drug_class,
                    "days_supply": days_supply,
                    "fill_date": str(fill_date),
                    "quantity_dispensed": random.randint(30, 90),
                    "pharmacy_npi": _random_id("NPI", 10),
                    "prescriber_npi": _random_id("PRV", 10),
                    "paid_amount": round(random.uniform(5.0, 800.0), 2),
                    "measurement_year": 2024,
                }
            )
    return pd.DataFrame(rows)


