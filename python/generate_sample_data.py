def generate_mtm_interventions(members_df: pd.DataFrame, seed: int = 42) -> pd.DataFrame:
    """
    Generate synthetic MTM/intervention records.
    Grain: one row per intervention event.
    PHI level: HIGH (member_id).
    """
    random.seed(seed)
    intervention_types = ["CMR", "TMR", "PHARMACIST_CONSULT", "PROVIDER_OUTREACH", "MEMBER_OUTREACH"]
    outcomes = ["COMPLETED", "REFUSED", "NO_RESPONSE", "SCHEDULED", "PENDING"]
    rows = []
    for _, member in members_df.sample(frac=0.4, random_state=seed).iterrows():
        n_interventions = random.randint(1, 3)
        for _ in range(n_interventions):
            itype = random.choice(intervention_types)
            rows.append(
                {
                    "intervention_id": str(uuid.uuid4()),
                    "member_id": member["member_id"],
                    "contract_id": member["contract_id"],
                    "intervention_type": itype,
                    "intervention_date": str(_random_date(date(2024, 1, 1), date(2024, 12, 31))),
                    "outcome": random.choice(outcomes),
                    "pharmacist_npi": _random_id("PHR", 10),
                    "notes_available": random.choice([True, False]),
                    "measurement_year": 2024,
                }
            )
    return pd.DataFrame(rows)


def generate_patient_safety_gaps(members_df: pd.DataFrame, seed: int = 42) -> pd.DataFrame:
    """
    Generate synthetic patient safety gap records.
    Grain: one row per member per measure gap identified.
    PHI level: HIGH (member_id).

    NOTE: Gap logic here is illustrative. Real gap detection must use
    validated measure specifications.
    """
    random.seed(seed)
    measure_codes = ["HRM_V1", "SUPD_V1", "PDC_STATIN", "PDC_RASA", "PDC_DIAB", "DDI_WARFARIN_NSAID"]
    gap_statuses = ["OPEN", "CLOSED", "IN_PROGRESS", "ESCALATED"]
    rows = []
    for _, member in members_df.iterrows():
        n_gaps = random.randint(0, 3)
        for _ in range(n_gaps):
            measure_code = random.choice(measure_codes)
            rows.append(
                {
                    "gap_id": str(uuid.uuid4()),
                    "member_id": member["member_id"],
                    "contract_id": member["contract_id"],
                    "measure_code": measure_code,
                    "gap_detected_date": str(_random_date(date(2024, 1, 1), date(2024, 11, 30))),
                    "gap_status": random.choice(gap_statuses),
                    "risk_score": round(random.uniform(0.1, 1.0), 3),
                    "evidence_summary": f"Synthetic gap for {measure_code}",
                    "measurement_year": 2024,
                }
            )
    return pd.DataFrame(rows)
